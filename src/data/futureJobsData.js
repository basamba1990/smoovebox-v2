// Fonction utilitaire pour générer le prompt final en appliquant la logique PINN-like
// (Contraintes + Créativité)
export function generatePinnPrompt(jobId, videoGenerator = 'Sora/Runway/Pika') {
  const job = futureJobsData.find(j => j.id === jobId);
  if (!job) return "Job not found.";

  // La logique PINN-like est l'application des contraintes (KeyTasks, EmergingTech, VisualElements)
  // pour générer un prompt créatif (basePrompt) qui est à la fois visuellement riche et
  // ancré dans la réalité du marché de l'emploi (WEF).

  const constraints = `
    Contraintes (WEF 2025):
    - Tâches clés: ${job.keyTasks}
    - Technologies: ${job.emergingTech}
    - Éléments visuels: ${job.visualElements}
  `;

  const finalPrompt = `
    PROMPT PINN-LIKE POUR ${videoGenerator} (Métier: ${job.title}, Année: ${job.year})

    ${job.basePrompt}

    ---
    Vérification des contraintes (PINN-like):
    - Le scénario doit intégrer la tâche clé: "${job.keyTasks}".
    - La technologie centrale à visualiser est: "${job.emergingTech}".
    - Le style visuel doit respecter: "${job.visualElements}".
    - Durée: 25-30 secondes.
  `;

  return finalPrompt.trim();
}

