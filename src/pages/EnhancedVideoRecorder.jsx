import React, { useState, useRef, useEffect } from 'react';
import { toast } from 'sonner';
import { Button } from './ui/button-enhanced.jsx';

const EnhancedVideoRecorder = ({ onRecordingComplete, user }) => {
  const [isRecording, setIsRecording] = useState(false);
  const [hasCameraAccess, setHasCameraAccess] = useState(false);
  const [cameraError, setCameraError] = useState(null);
  const [recordingTime, setRecordingTime] = useState(0);
  const videoRef = useRef(null);
  const mediaRecorderRef = useRef(null);
  const streamRef = useRef(null);
  const chunksRef = useRef([]);
  const timerRef = useRef(null);

  // ‚úÖ CORRECTION : Initialisation robuste de la cam√©ra
  const initializeCamera = async () => {
    try {
      setCameraError(null);
      
      // Arr√™ter le flux existant
      if (streamRef.current) {
        streamRef.current.getTracks().forEach(track => track.stop());
      }

      // Demander l'acc√®s √† la cam√©ra avec contraintes am√©lior√©es
      const stream = await navigator.mediaDevices.getUserMedia({
        video: {
          width: { ideal: 1280 },
          height: { ideal: 720 },
          facingMode: 'user'
        },
        audio: true
      });

      streamRef.current = stream;
      
      if (videoRef.current) {
        videoRef.current.srcObject = stream;
        
        // ‚úÖ CORRECTION : Attendre que la vid√©o soit charg√©e
        await new Promise((resolve, reject) => {
          if (videoRef.current) {
            videoRef.current.onloadedmetadata = () => {
              resolve();
            };
            videoRef.current.onerror = () => {
              reject(new Error('Erreur de chargement de la vid√©o'));
            };
            
            // Timeout de s√©curit√©
            setTimeout(() => {
              if (videoRef.current?.readyState >= 1) {
                resolve();
              } else {
                reject(new Error('Timeout de chargement vid√©o'));
              }
            }, 3000);
          }
        });
        
        // Forcer la lecture avec gestion d'erreur
        try {
          await videoRef.current.play();
          setHasCameraAccess(true);
          console.log('‚úÖ Cam√©ra activ√©e avec succ√®s');
          toast.success('Cam√©ra activ√©e ! Vous pouvez maintenant vous enregistrer.');
        } catch (playError) {
          console.error('Erreur lecture vid√©o:', playError);
          setCameraError('Erreur de lecture vid√©o');
          toast.error('Probl√®me d\'affichage vid√©o');
        }
      }
    } catch (error) {
      console.error('‚ùå Erreur cam√©ra:', error);
      let errorMessage = `Erreur cam√©ra: ${error.message}`;
      
      // Gestion des erreurs sp√©cifiques
      if (error.name === 'NotAllowedError') {
        errorMessage = 'Veuillez autoriser l\'acc√®s √† la cam√©ra dans les param√®tres de votre navigateur';
        toast.info('Autorisation cam√©ra requise');
      } else if (error.name === 'NotFoundError') {
        errorMessage = 'Aucune cam√©ra d√©tect√©e. V√©rifiez votre connexion.';
        toast.error('Aucune cam√©ra d√©tect√©e');
      } else if (error.name === 'NotReadableError') {
        errorMessage = 'La cam√©ra est d√©j√† utilis√©e par une autre application';
        toast.error('Cam√©ra indisponible');
      }
      
      setCameraError(errorMessage);
      setHasCameraAccess(false);
    }
  };

  // ‚úÖ CORRECTION : V√©rification au montage
  useEffect(() => {
    initializeCamera();
    
    return () => {
      // Nettoyage complet
      if (streamRef.current) {
        streamRef.current.getTracks().forEach(track => {
          track.stop();
          track.enabled = false;
        });
        streamRef.current = null;
      }
      if (timerRef.current) {
        clearInterval(timerRef.current);
      }
    };
  }, []);

  const startRecording = async () => {
    if (!streamRef.current) {
      toast.error('Cam√©ra non disponible');
      await initializeCamera();
      if (!streamRef.current) return;
    }

    try {
      const options = { mimeType: 'video/webm;codecs=vp9,opus' };
      const mediaRecorder = new MediaRecorder(streamRef.current, options);
      mediaRecorderRef.current = mediaRecorder;
      chunksRef.current = [];

      mediaRecorder.ondataavailable = (e) => {
        if (e.data.size > 0) {
          chunksRef.current.push(e.data);
        }
      };

      mediaRecorder.onstop = () => {
        const blob = new Blob(chunksRef.current, { type: 'video/webm' });
        if (onRecordingComplete) {
          onRecordingComplete(blob);
        }
        setRecordingTime(0);
        if (timerRef.current) {
          clearInterval(timerRef.current);
        }
      };

      mediaRecorder.onerror = (event) => {
        console.error('Erreur enregistrement:', event);
        toast.error('Erreur lors de l\'enregistrement');
        setIsRecording(false);
        setRecordingTime(0);
        if (timerRef.current) {
          clearInterval(timerRef.current);
        }
      };

      // D√©marrer l'enregistrement
      mediaRecorder.start(1000); // Collecte des donn√©es chaque seconde
      setIsRecording(true);
      
      // Timer d'enregistrement
      setRecordingTime(0);
      timerRef.current = setInterval(() => {
        setRecordingTime(prev => {
          if (prev >= 180) { // Maximum 3 minutes
            stopRecording();
            return 180;
          }
          return prev + 1;
        });
      }, 1000);

      toast.success('üé• Enregistrement d√©marr√© !');
    } catch (error) {
      console.error('‚ùå Erreur d√©marrage enregistrement:', error);
      toast.error('Erreur lors du d√©marrage de l\'enregistrement');
    }
  };

  const stopRecording = () => {
    if (mediaRecorderRef.current && isRecording) {
      mediaRecorderRef.current.stop();
      setIsRecording(false);
      
      if (timerRef.current) {
        clearInterval(timerRef.current);
      }
      
      toast.success('‚úÖ Enregistrement termin√© !');
    }
  };

  const retryCamera = async () => {
    setCameraError(null);
    await initializeCamera();
  };

  const formatTime = (seconds) => {
    const mins = Math.floor(seconds / 60);
    const secs = seconds % 60;
    return `${mins.toString().padStart(2, '0')}:${secs.toString().padStart(2, '0')}`;
  };

  return (
    <div className="space-y-6 p-6 border-2 border-gray-300 rounded-xl bg-white shadow-lg">
      <h3 className="text-2xl font-bold text-gray-800 text-center">
        üé• Enregistrement Vid√©o SpotBulle
      </h3>
      
      {/* ‚úÖ CORRECTION : Avertissement cam√©ra am√©lior√© */}
      {!hasCameraAccess && (
        <div className="bg-yellow-50 border-2 border-yellow-300 rounded-lg p-4 animate-pulse">
          <div className="flex items-center justify-between">
            <div className="flex-1">
              <p className="text-yellow-800 font-semibold text-lg">üì∑ Cam√©ra non d√©tect√©e</p>
              <p className="text-yellow-700 text-sm mt-1">
                {cameraError || 'V√©rifiez que votre cam√©ra est branch√©e et autoris√©e'}
              </p>
              <p className="text-yellow-600 text-xs mt-2">
                üí° Conseil : Rafra√Æchissez la page et autorisez l'acc√®s √† la cam√©ra
              </p>
            </div>
            <Button
              onClick={retryCamera}
              className="bg-yellow-500 hover:bg-yellow-600 text-white px-4 py-2 rounded-lg font-semibold"
            >
              üîÑ R√©essayer
            </Button>
          </div>
        </div>
      )}

      {/* √âl√©ment vid√©o avec gestion d'erreur am√©lior√©e */}
      <div className="relative bg-black rounded-xl overflow-hidden shadow-2xl">
        <video
          ref={videoRef}
          autoPlay
          muted
          playsInline
          className="w-full h-auto max-h-96 object-contain bg-gray-900"
          onError={() => {
            setCameraError('Erreur de flux vid√©o - R√©initialisation en cours...');
            setTimeout(retryCamera, 2000);
          }}
          onLoadStart={() => console.log('üîÑ Chargement vid√©o d√©marr√©')}
          onCanPlay={() => console.log('‚úÖ Vid√©o pr√™te √† jouer')}
          onPlaying={() => console.log('üé¨ Vid√©o en cours de lecture')}
        />
        
        {/* Overlay si pas de cam√©ra */}
        {!hasCameraAccess && (
          <div className="absolute inset-0 bg-gray-900 flex items-center justify-center">
            <div className="text-white text-center p-8">
              <div className="text-6xl mb-4">üì∑</div>
              <p className="text-xl font-semibold mb-2">Cam√©ra en attente...</p>
              <p className="text-gray-300">Veuillez autoriser l'acc√®s √† votre cam√©ra</p>
              <Button
                onClick={retryCamera}
                className="mt-4 bg-blue-600 hover:bg-blue-700 text-white px-6 py-3 rounded-lg"
              >
                üîç D√©tecter la cam√©ra
              </Button>
            </div>
          </div>
        )}

        {/* Indicateur d'enregistrement avec timer */}
        {isRecording && (
          <div className="absolute top-4 right-4 flex items-center space-x-3 bg-red-500 text-white px-4 py-2 rounded-full shadow-lg animate-pulse">
            <div className="w-3 h-3 bg-white rounded-full animate-pulse"></div>
            <span className="font-semibold">ENREGISTREMENT</span>
            <span className="font-mono bg-red-600 px-2 py-1 rounded">
              {formatTime(recordingTime)}
            </span>
          </div>
        )}

        {/* Indicateur de statut cam√©ra */}
        {hasCameraAccess && !isRecording && (
          <div className="absolute top-4 left-4 bg-green-500 text-white px-3 py-1 rounded-full text-sm font-semibold">
            ‚úÖ Cam√©ra active
          </div>
        )}
      </div>

      {/* ‚úÖ CORRECTION : Contr√¥les am√©lior√©s */}
      <div className="flex flex-col sm:flex-row justify-center items-center space-y-4 sm:space-y-0 sm:space-x-6">
        {!isRecording ? (
          <Button
            onClick={startRecording}
            disabled={!hasCameraAccess}
            className="bg-red-500 hover:bg-red-600 text-white px-8 py-4 text-lg font-semibold rounded-xl shadow-lg transition-all disabled:bg-gray-400 disabled:cursor-not-allowed flex items-center space-x-3"
          >
            <span>üé¨</span>
            <span>Commencer l'enregistrement</span>
          </Button>
        ) : (
          <Button
            onClick={stopRecording}
            className="bg-gray-500 hover:bg-gray-600 text-white px-8 py-4 text-lg font-semibold rounded-xl shadow-lg transition-all flex items-center space-x-3"
          >
            <span>‚èπÔ∏è</span>
            <span>Arr√™ter l'enregistrement</span>
          </Button>
        )}
        
        <Button
          onClick={retryCamera}
          className="bg-blue-500 hover:bg-blue-600 text-white px-6 py-3 rounded-lg font-semibold transition-all flex items-center space-x-2"
        >
          <span>üîÑ</span>
          <span>R√©initialiser cam√©ra</span>
        </Button>
      </div>

      {/* ‚úÖ CORRECTION : Instructions am√©lior√©es */}
      <div className="bg-blue-50 border border-blue-200 rounded-lg p-4">
        <h4 className="font-semibold text-blue-800 mb-2">üí° Conseils pour un bon enregistrement :</h4>
        <ul className="text-blue-700 text-sm space-y-1">
          <li>‚Ä¢ Assurez-vous d'√™tre dans un endroit bien √©clair√©</li>
          <li>‚Ä¢ Regardez droit dans la cam√©ra</li>
          <li>‚Ä¢ Parlez clairement et distinctement</li>
          <li>‚Ä¢ Maximum 3 minutes par enregistrement</li>
          <li>‚Ä¢ Votre vid√©o sera priv√©e par d√©faut</li>
        </ul>
      </div>
    </div>
  );
};

export default EnhancedVideoRecorder;
